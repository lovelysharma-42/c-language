//a program to show how bitwise operator works
#include <stdio.h>

int main(void) {
        int a=34,b=56;
	printf("\n a & b = %d",a&b);
	printf("\n a | b = %d",a|b);
	printf("\n ~a = %d",~a);
	printf("\n ~b = %d",~b);
	printf("\n a ^ b = %d",a^b);
	printf("\n a << 1 = %d",a<<1);
	printf("\n b >> 1 = %d",b>>1);
	return 0;
}

