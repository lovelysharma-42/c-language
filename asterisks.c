//a program to print 20 asterisks in a horizontal line using while loop and another while loop for 20 vertical asterisks.
#include <stdio.h>
int main(void) 
{
        int i = 1;
        while(i<=20)
        {
          printf("*");
          i++;
        }
        int j=1;
        while(j<=20)
        {
          printf("*\n");
          j++;
        }
        
        return 0;
}        
