//PROGRAM TO ILLUSTRATE THE USE OF UNARY PREFIX INCREMENT AND DECREMENT OPERATORS
#include <stdio.h>

int main(void) {
        int count = 5;
        //using prefix increment operator
	printf("\n The value of count =%d",count);
	printf("\n The value of count =%d",++count);
	printf("\n The new value of count =%d",count);
	//using prefix decrement operator
	printf("\n The value of count =%d",count);
	printf("\n The value of count =%d",--count);
	printf("\n The new value of count =%d",count);
	
	return 0;
}

