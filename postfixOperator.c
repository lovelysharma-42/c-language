//PROGRAM TO ILLUSTRATE THE USE OF UNARY POSTFIX INCREMENT AND DECREMENT OPERATORS
#include <stdio.h>

int main(void) {
        int count = 5;
        //using postfix increment operator
	printf("\n The value of count =%d",count);
	printf("\n The value of count =%d",count++);
	printf("\n The new value of count =%d",count);
	//using postfix decrement operator
	printf("\n The value of count =%d",count);
	printf("\n The value of count =%d",count--);
	printf("\n The new value of count =%d",count);
	
	return 0;
}

