#include <stdio.h>
int main(void) 
{
        int option =1;
        switch(option)
        {
         case 1:
         printf("\n In case 1");
         break;
 
         case 2:
           printf("\n In case 2");
         break;
 
         default:
           printf("\n In case default");
         }
        return 0;
}        
