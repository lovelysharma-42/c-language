//a program which prints the first n numbers using for loop.
#include <stdio.h>
int main(void) 
{
        int i,n;
        
        printf("\n\n enter any number: ");
        scanf("%d",&n);
        for(i=1;i<=n;i++)
        {
         
         printf("\n%d",i);
             
        }
        return 0;
}        
